<h2>Що таке алгоритм сортування?</h2>
<p>Алгоритм сортування — це набір кроків для впорядкування елементів колекції. Здається тривіально, але за цим стоїть ціла теорія складності, компромісів між часом і пам''яттю та ситуативних переваг різних підходів.</p>
<h2>Чому це важливо?</h2>
<p>Розуміння сортування дає вам базу для більш складних концепцій: дерева, купи, графи — всюди є подібні патерни. Плюс, на технічних інтерв''ю в Big Tech питають саме це.</p>
<h2>Топ-3 алгоритми які треба знати</h2>
<p><strong>Quick Sort</strong> — O(n log n) в середньому. Використовується в більшості реальних реалізацій. Ідея: вибрати «pivot» і розділити масив на менші та більші елементи рекурсивно.</p>
<p><strong>Merge Sort</strong> — O(n log n) гарантовано. Стабільний. Відмінно підходить для зв''язних списків та зовнішнього сортування великих файлів.</p>
<p><strong>Heap Sort</strong> — O(n log n), O(1) пам''яті. Використовує структуру двійкової купи. Рідше використовується на практиці але важливий теоретично.</p>
